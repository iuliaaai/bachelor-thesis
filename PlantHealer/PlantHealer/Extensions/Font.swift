import SwiftUI

extension Font {
    static func customFont(size: CGFloat) -> Font {
        Font.custom("Montserrat-Regular", size: size)
    }
    
    static func montserratMedium21() -> Font {
        Font.custom("Montserrat-Medium", size: 21)
    }
    
    static func montserratMedium18() -> Font {
        Font.custom("Montserrat-Medium", size: 18)
    }
    
    static func montserratMedium16() -> Font {
        Font.custom("Montserrat-Medium", size: 16)
    }
    
    static func montserratMedium14() -> Font {
        Font.custom("Montserrat-Medium", size: 14)
    }
    
    static func montserratMedium12() -> Font {
        Font.custom("Montserrat-Medium", size: 12)
    }
    
    static func montserratBlack21() -> Font {
        Font.custom("Montserrat-Black", size: 21)
    }
    
    static func montserratBold21() -> Font {
        Font.custom("Montserrat-Bold", size: 21)
    }
    
    static func montserratBold12() -> Font {
        Font.custom("Montserrat-Bold", size: 12)
    }
    
    static func montserratBold18() -> Font {
        Font.custom("Montserrat-Bold", size: 18)
    }
    
    static func montserratRegular21() -> Font {
        Font.custom("Montserrat-Regular", size: 21)
    }
    
    static func montserratRegular18() -> Font {
        Font.custom("Montserrat-Regular", size: 18)
    }
}
